const MATERIAL_TO_WEAPONS = new Map<string, string[]>();
for (const weapon of WEAPONS) {
  for (const mat of weapon.materials) {
    const list = MATERIAL_TO_WEAPONS.get(mat) || [];
    list.push(weapon.name);
    MATERIAL_TO_WEAPONS.set(mat, list);
  }
}

export function getWeaponsForMaterial(material: string): string[] {
  return MATERIAL_TO_WEAPONS.get(material) || [];
}

export function getWeaponsByType(type: WeaponType): Weapon[] {
  return WEAPONS.filter((w) => w.weaponType === type);
}

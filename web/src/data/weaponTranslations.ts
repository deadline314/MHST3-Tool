export function translateMaterialName(jp: string): string {
  if (MATERIAL_ZH[jp]) return MATERIAL_ZH[jp];
  if (WEAPON_NAME_ZH[jp]) return WEAPON_NAME_ZH[jp];
  if (ARMOR_NAME_ZH[jp]) return ARMOR_NAME_ZH[jp];

  if (jp.endsWith("の書")) {
    const base = jp.slice(0, -2);
    const wZh = WEAPON_NAME_ZH[base] || ARMOR_NAME_ZH[base];
    if (wZh) return wZh + "之書";
    return jp;
  }

  const noIdx = jp.indexOf("の");
  if (noIdx > 0) {
    const monsterPart = jp.slice(0, noIdx);
    const partPart = jp.slice(noIdx + 1);
    const mZh = MONSTER_PART_ZH[monsterPart];
    const pZh = PART_ZH[partPart];
    if (mZh && pZh) return mZh + "的" + pZh;
    if (mZh) return mZh + "的" + partPart;
  }

  return jp;
}

